// Java program to demonstrate basic arithmetic operators
class ArithmeticOperators {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;

        // Print sum of a and b using + operator
        System.out.println("Sum: " + (a + b));

        // Print difference of a and b using - operator
        System.out.println("Difference: " + (a - b));

        // Print product of a and b using * operator
        System.out.println("Product: " + (a * b));

        // Print quotient of a and b using / operator
        System.out.println("Quotient: " + (a / b));

        // Print remainder of a and b using % operator
        System.out.println("Remainder: " + (a % b));
    }
}
