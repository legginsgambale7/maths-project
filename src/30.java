public class MathOperations {
    public static void main(String[] args) {
        // Example of performing basic arithmetic operations
        int result1 = 5 + 3;
        System.out.println("The sum is: " + result1);

        double result2 = 7.5 - 4.0;
        System.out.println("The difference is: " + result2);

        int result3 = 10 % 3;
        System.out.println("The remainder is: " + result3);
    }
}
