public class MathOperations {
    public static void main(String[] args) {
        // Example of adding two numbers
        int num1 = 5;
        int num2 = 3;
        int sum = num1 + num2;
        System.out.println("The sum is: " + sum);
        
        // Example of subtracting two numbers
        int diff = 8 - 4;
        System.out.println("The difference is: " + diff);
    }
}
